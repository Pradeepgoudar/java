public class swapping{
    public static void main(String [] args){
        int a=2;
        int b=3;
        int temp;
        System.out.print("before swapping "+a+" "+b);
        System.out.println();
        temp=a;
        a=b;
        b=temp;
        System.out.print("after swapping "+a+" "+b);
    }
}